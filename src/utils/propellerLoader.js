// utils/propellerLoader.js
export const loadPropellerAd = (zoneId) => {
    const script = document.createElement("script");
    script.src = `https://upgulpinon.com/1?z=${zoneId}`;
    script.async = true;
    script.setAttribute("data-cfasync", "false");
    document.body.appendChild(script);
  };          

